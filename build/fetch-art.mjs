// Build step: deck.json -> src/albums.json + src/albums.js + src/img/<id>.jpg
//
// Per album: iTunes (artwork + Apple link + year) is primary; Wikidata supplies
// the Spotify id (best-effort); a flat placeholder cover guarantees no row is
// ever missing art. Runtime is offline, so albums are inlined into albums.js as
// window.ALBUMS (the webview cannot fetch a JSON file under every host).
import { readFile, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { searchItunes, buildLinks } from './lib/itunes.mjs';
import { mbReleaseGroup } from './lib/musicbrainz.mjs';
import { downloadImage, resizeCover, placeholderCover, caaUrl } from './lib/covers.mjs';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SRC = join(ROOT, 'src');
const IMG = join(SRC, 'img');
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function buildAlbum(row) {
  const out = {
    id: row.id,
    artist: row.artist,
    title: row.title,
    year: row.year,
    genre: row.genre,
    img: `img/${row.id}.jpg`,
  };
  const imgPath = join(IMG, `${row.id}.jpg`);

  let itunes = null;
  try {
    itunes = await searchItunes(row.artist, row.title);
  } catch (e) {
    console.warn(`  ${row.id}: iTunes failed: ${e.message}`);
  }

  // Cover: iTunes artwork first, then Cover Art Archive (via MusicBrainz), else
  // a placeholder.
  let gotCover = false;
  if (itunes?.artworkUrl) {
    try {
      await resizeCover(await downloadImage(itunes.artworkUrl), imgPath);
      gotCover = true;
    } catch (e) {
      console.warn(`  ${row.id}: iTunes cover download failed: ${e.message}`);
    }
  }
  if (!gotCover) {
    const mbid = await mbReleaseGroup(row.artist, row.title);
    if (mbid) {
      try {
        await resizeCover(await downloadImage(caaUrl(mbid, 500)), imgPath);
        gotCover = true;
        console.log(`  ${row.id}: cover from Cover Art Archive`);
      } catch (e) {
        console.warn(`  ${row.id}: CAA cover failed: ${e.message}`);
      }
    }
  }
  if (!gotCover) {
    await placeholderCover(imgPath, String(row.year));
    console.warn(`  ${row.id}: using placeholder cover`);
  }

  // Apple link comes from iTunes; Spotify + YouTube are search URLs (Spotify
  // gives no public id API, and a search URL beats risking a wrong direct link).
  out.links = buildLinks(row, { appleUrl: itunes?.appleUrl });
  out._ok = gotCover; // resume marker: real cover fetched
  return out;
}

async function main() {
  const deck = JSON.parse(await readFile(join(ROOT, 'deck.json'), 'utf8'));

  // Resume: reuse entries that already fetched a real cover; only (re)fetch the
  // rest. iTunes rate-limits hard, so a clean run may take several passes.
  const prevPath = join(SRC, 'albums.json');
  const prev = new Map();
  if (existsSync(prevPath)) {
    for (const a of JSON.parse(await readFile(prevPath, 'utf8'))) prev.set(a.id, a);
  }

  console.log(`Building ${deck.length} albums...`);
  const albums = [];
  let n = 0;
  let fetched = 0;
  for (const row of deck) {
    n += 1;
    const cached = prev.get(row.id);
    if (cached && cached._ok && existsSync(join(IMG, `${row.id}.jpg`))) {
      albums.push(cached);
      continue; // already has a real cover — skip the network
    }
    process.stdout.write(`[${n}/${deck.length}] ${row.id} ${row.artist} — ${row.title}\n`);
    albums.push(await buildAlbum(row));
    fetched += 1;
    await sleep(3500); // ~17/min, under iTunes' ~20/min cap
  }

  await writeFile(prevPath, JSON.stringify(albums, null, 2));
  await writeFile(
    join(SRC, 'albums.js'),
    `// Generated by build/fetch-art.mjs — do not edit.\nwindow.ALBUMS = ${JSON.stringify(
      albums.map(({ _ok, ...a }) => a),
    )};\n`,
  );
  const placeholders = albums.filter((a) => !a._ok).length;
  console.log(`\nWrote albums.json + albums.js (${albums.length} albums; fetched ${fetched} this pass).`);
  if (placeholders) console.log(`${placeholders} still on placeholder covers — re-run \`npm run build\` to retry them.`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
